`timescale 1ns / 1ps

module proc_tb;

    // הגדרת חוטים לחיבור המעבד
    logic [8:0] DIN;
    logic       Resetn;
    logic       Clock;
    logic       Run;
    logic       Done;
    logic [8:0] BusWires;

    // יצירת מופע (Instantiation) של המעבד
    proc DUT (
        .DIN(DIN),
        .Resetn(Resetn),
        .Clock(Clock),
        .Run(Run),
        .Done(Done),
        .BusWires(BusWires)
    );

    // יצירת שעון: מחזור שעון כל 20 יחידות זמן
    always #10 Clock = ~Clock;

    initial begin
        // ==========================================
        // שלב 1: אתחול המערכת (Reset)
        // ==========================================
        Clock = 0;
        Run = 0;
        DIN = 9'b0;
        Resetn = 0; // לחיצה על ריסט
        #40;        
        Resetn = 1; // שחרור הריסט
        Run = 1;    // הפעלת המעבד לתמיד

        // ==========================================
        // שלב 2: mvi R1, #15
        // ==========================================
        DIN = 3'b001_001_000; // פקודת mvi ל-R1
        #20;                  // שלב T0 (הפקודה נכנסת ל-IR)
        DIN = 9'd15;          // הנתון 15
        #20;                  // שלב T1 (הנתון נטען ל-R1)

        // ==========================================
        // שלב 3: mvi R2, #5
        // ==========================================
        DIN = 3'b001_010_000; // פקודת mvi ל-R2
        #20;
        DIN = 9'd5;           // הנתון 5
        #20;

        // ==========================================
        // שלב 4: mv R4, R1
        // ==========================================
        DIN = 3'b000_100_001; // העתקה מ-R1 ל-R4
        #40;                  // לוקח שני מחזורי שעון (T0 לקריאה, T1 לביצוע)

        // ==========================================
        // שלב 5: add R1, R2
        // ==========================================
        DIN = 3'b010_001_010; 
        #80;                  // הפקודה לוקחת 4 מחזורים (T0, T1, T2, T3)

        // ==========================================
        // שלב 6: sub R1, R2
        // ==========================================
        DIN = 3'b011_001_010; 
        #80;                  // שוב, ממתינים 4 מחזורים

        // ==========================================
        // שלבים 7+8: הכנה וביצוע הבונוס specialMult
        // ==========================================
        // התנאי המקדים מהדוח: טעינת 4 ל-R0
        DIN = 3'b001_000_000; // mvi R0
        #20;
        DIN = 9'd4;           // הנתון 4
        #20;

        // הבדיקה עצמה: כפל ב-3.5
        DIN = 3'b101_000_101; // specialMult R0, R5
        #80;                  // המתנה ל-T3 לסיום הפעולה (התוצאה ב-R5 תהיה 14)

        // ==========================================
        // שלב 9: הבונוס השני ones
        // ==========================================
        // ב-R1 יושב הערך 15 מפעולת החיסור קודם. 15 = 000001111 (ארבעה אחדות)
        DIN = 3'b100_001_110; // ones R1, R6
        #80;                  // התוצאה ב-R6 תהיה 4

        // ==========================================
        // סיום הסימולציה
        // ==========================================
        #40;
        $stop;
    end

endmodule